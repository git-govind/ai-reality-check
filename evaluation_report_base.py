"""
evaluation_report_base.py
--------------------------
Shared base dataclass for all evaluation report types in the AI Reality Check
pipeline.

Subclasses
----------
  EvaluationReport       (text pipeline)   src/scoring/scoring_engine.py
  ImageEvaluationReport  (image pipeline)  image_evaluator/datatypes.py

Provides a common structural contract for the pipeline, the Streamlit UI, and
any future serialisation / logging integrations.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any, Dict, List


@dataclass
class EvaluationReportBase:
    """Common fields shared by all evaluation report types.

    Fields
    ------
    metadata : dict
        Free-form key/value information about the evaluated input
        (e.g. image format and dimensions, source model name, word count).
        Populated by each concrete subclass; empty dict by default.

    scores : dict
        Named sub-scores as a flat ``{name: float}`` mapping on the 0–100
        scale.  Populated by each concrete subclass's aggregation function.

    issues : list[str]
        Human-readable descriptions of detected anomalies or problems
        collected across all pipeline steps.
        In :class:`EvaluationReport` (text) this is exposed via the
        backward-compatible ``all_issues`` property alias.

    evidence : dict
        Structured signals grouped by pipeline step; the primary payload
        consumed by the Streamlit UI to render detailed breakdowns.

    grade : str
        Single letter grade derived from the pipeline's primary score
        (A / B / C / D / F).  Set by each subclass's aggregation function.

    explanation : str
        A short, human-readable paragraph summarising the verdict, key
        strengths, major issues, and supporting evidence.  Generated by
        :func:`explanation_generator.generate_explanation` and set by each
        pipeline's aggregation function after the report is assembled.
    """

    metadata:    Dict[str, Any] = field(default_factory=dict)
    scores:      Dict[str, Any] = field(default_factory=dict)
    issues:      List[str]      = field(default_factory=list)
    evidence:    Dict[str, Any] = field(default_factory=dict)
    grade:       str            = ""
    explanation: str            = ""
